// Print natural numbers from 101 to 200 in a lexicographical order

class Solution {
    public static void main(String args[])
    {
        System.out.print("\nNatural numbers from 101 to 200 in a lexicographical order: ");
        for (int i = 100; i <= 200; i++)
        {
            System.out.print(i + " ");
        }

        System.out.println();
    }
}