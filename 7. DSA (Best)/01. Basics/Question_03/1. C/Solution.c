// Print natural numbers from 101 to 200 in a lexicographical order

#include <stdio.h>

void main()
{
    printf("\nNatural numbers from 101 to 200 in a lexicographical order: ");

    for (int i = 101; i <= 200; i++)
    {
        printf("%d ", i);
    }

    printf("\n");
    // printf("%c", '\n');
}