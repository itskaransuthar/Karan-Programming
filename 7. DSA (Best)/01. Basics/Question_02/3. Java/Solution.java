// Print natural numbers in reverse order from 200 to 101

class Solution {
    public static void main(String[] args) {
        System.out.print("\nNatural numbers in reverse order from 200 to 101: ");

        for (int i = 200; i >= 101; i--)
        {
            System.out.print(i + " ");
        }

        System.out.println();
    }
}